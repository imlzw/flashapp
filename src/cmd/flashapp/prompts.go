func composePromptWithTemplate(req agentRequest) string {
	prompt := UniversalPromptTemplate

	// 替换占位符
	prompt = strings.ReplaceAll(prompt, "{{TITLE}}", req.Title)
	prompt = strings.ReplaceAll(prompt, "{{PROMPT}}", req.Prompt)

	// 处理重构逻辑
	if req.ExistingHTML != "" {
		refactorBlock := "\n【重构任务】：检测到旧版代码。请基于旧逻辑进行进化，如果旧版不符合上述对应分类的高标准准则，请进行彻底的 UI/UX 重构：\n" + req.ExistingHTML
		prompt = strings.ReplaceAll(prompt, "{{EXISTING_CODE_BLOCK}}", refactorBlock)
	} else {
		prompt = strings.ReplaceAll(prompt, "{{EXISTING_CODE_BLOCK}}", "")
	}

	return prompt
}
